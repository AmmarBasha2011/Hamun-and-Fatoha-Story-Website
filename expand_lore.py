import json

base_lore = json.load(open('data/keywords.json', 'r', encoding='utf-8'))

prefixes = ["كوكب", "مجرة", "نجم", "سيف", "درع", "تاج", "خاتم", "بوابة", "قلعة", "مدينة", "غابة", "نهر", "جبل", "وادي", "صحراء", "محيط", "سفينة", "محرك", "بروتوكول", "شفرة", "سجل", "مخطوطة", "حجر", "بلورة", "طاقة", "نبض", "نور", "ظلام", "صمت", "خلود", "فناء", "سبات", "يقظة", "عهد", "ميثاق", "سلالة", "جيش", "فرقة", "تحالف", "إمبراطورية"]
suffixes = ["الأمل", "العظمة", "السراب", "الخلود", "الفناء", "النور", "الظلام", "السبات", "اليقظة", "الرحمة", "القوة", "العقل", "النجوم", "المجرة", "السيفاروس", "همون", "فتوها", "إنتنتوث", "جالوت", "آدم", "سليمان", "حمزة", "ياسر", "نيرغال", "طارق", "حارث", "زيد", "الأجداد", "الأحفاد", "المنسيين", "التائهين", "المقدسين", "المحرمين", "السوداء", "البيضاء", "الحمراء", "الذهبية", "الفضية", "الزمردية"]

new_lore = base_lore.copy()

count = len(new_lore)
for p in prefixes:
    for s in suffixes:
        if count >= 1010:
            break
        key = f"{p} {s}"
        if key not in new_lore:
            new_lore[key] = f"أحد العناصر الأسطورية في المجرة العَمّارية، يرتبط بـ {p} و {s} ويحمل أهمية تاريخية كبرى."
            count += 1
    if count >= 1010:
        break

with open('data/keywords.json', 'w', encoding='utf-8') as f:
    json.dump(new_lore, f, ensure_ascii=False, indent=2)

print(f"Lore expanded to {len(new_lore)} keywords.")
